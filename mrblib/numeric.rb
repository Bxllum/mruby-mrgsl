#------------------------------------------------------------------------------------------
# * Numeric class
#------------------------------------------------------------------------------------------
class Numeric
  #----------------------------------------------------------------------------------------
  # * plus_point
  #----------------------------------------------------------------------------------------
  def plus_point(point)
    return ::MRGSL::Point.new(point.x + self, point.y + self, point.z + self)
  end

  #----------------------------------------------------------------------------------------
  # * minus_point
  #----------------------------------------------------------------------------------------
  def sub_point(point)
    return ::MRGSL::Point.new(point.x - self, point.y - self, point.z - self)
  end

  #----------------------------------------------------------------------------------------
  # * mul_point
  #----------------------------------------------------------------------------------------
  def mul_point(point)
    return ::MRGSL::Point.new(point.x * self, point.y * self, point.z * self)
  end

  #----------------------------------------------------------------------------------------
  # * divl_point
  #----------------------------------------------------------------------------------------
  def div_point(point)
    return ::MRGSL::Point.new(point.x / self, point.y / self, point.z / self)
  end
end