#============================================================================================
# * Game Module
#============================================================================================
module MRGSL
  #------------------------------------------------------------------------------------------
  # * Color class
  #------------------------------------------------------------------------------------------
  class Color
    #----------------------------------------------------------------------------------------
    # * Accessors
    #----------------------------------------------------------------------------------------
    attr_reader :r
    attr_reader :g
    attr_reader :b
    attr_reader :a
    #----------------------------------------------------------------------------------------
    # * Constructor
    #----------------------------------------------------------------------------------------
    def initialize(r = 0, g = 0, b = 0, a = 255)
      set(r,g,b,a)
    end

    #----------------------------------------------------------------------------------------
    # * set
    #----------------------------------------------------------------------------------------
    def set(r = 0, g = 0, b = 0, a = 255)
      @r = r < 0 ? 0 : (r > 255 ? 255 : r)
      @g = g < 0 ? 0 : (g > 255 ? 255 : g)
      @b = b < 0 ? 0 : (b > 255 ? 255 : b)
      @a = a < 0 ? 0 : (a > 255 ? 255 : a)
      self
    end

    #----------------------------------------------------------------------------------------
    # * R set
    #----------------------------------------------------------------------------------------
    def r=(val)
      set(val, @g, @b,@a)
      self
    end

    #----------------------------------------------------------------------------------------
    # * g set
    #----------------------------------------------------------------------------------------
    def g=(val)
      set(@r, val, @b,@a)
      self
    end

    #----------------------------------------------------------------------------------------
    # * b set
    #----------------------------------------------------------------------------------------
    def b=(val)
      set(@r, @g, val,@a)
      self
    end

    #----------------------------------------------------------------------------------------
    # * a set
    #----------------------------------------------------------------------------------------
    def b=(val)
      set(@r, @g, @b,val)
      self
    end
  end
end
