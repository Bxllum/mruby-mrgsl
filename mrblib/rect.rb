module MRGSL
  #--------------------------------------------------------------------------------------------
  # * Rect class
  #---------t-----------------------------------------------------------------------------------
  class Rect
    #----------------------------------------------------------------------------------------
    # * Accessors
    #----------------------------------------------------------------------------------------
    attr_accessor :x
    attr_accessor :y
    attr_accessor :width
    attr_accessor :height
    #----------------------------------------------------------------------------------------
    # * Constructor
    #----------------------------------------------------------------------------------------
    def initialize(x = 0, y = 0, width = 0, height = 0)
      set(x,y,width, height)
    end
    #----------------------------------------------------------------------------------------
    # * set
    #----------------------------------------------------------------------------------------
    def set(x = 0, y = 0, width = 0, height = 0)
      @x = x
      @y = y
      @width = width
      @height = height
    end
    #----------------------------------------------------------------------------------------
    # * max_x
    #----------------------------------------------------------------------------------------
    def max_x
      return self.x + self.width
    end
    #----------------------------------------------------------------------------------------
    # * max_y
    #----------------------------------------------------------------------------------------
    def max_y
      return self.y + self.height
    end
    #----------------------------------------------------------------------------------------
    # * contains a point
    #----------------------------------------------------------------------------------------
    def contains_point?(aPoint)
      aPoint.x.between?(self.x, self.max_x) and aPoint.y.bewteen?(self.y, self.max_y)
    end
    #----------------------------------------------------------------------------------------
    # * fits in a rect
    #----------------------------------------------------------------------------------------
    def fits_in?(aRect)
      self.width < aRect.width and self.height < aRect.heightcle
    end
  end
end
